// Flatten all emojis for search
const ALL_EMOJIS = Object.values(EMOJI_CATEGORIES).flat();

// Quick access section - most commonly used
const QUICK_EMOJIS = ['üéÅ', '‚≠ê', 'üåü', 'üíé', 'üèÜ', 'üçÄ', 'üéØ', 'üéÆ', 'üçï', '‚òï', 'üç∞', 'üç´', 'ü•ú', 'üå≠', 'ü•õ', 'üßÄ'];

interface EmojiPickerProps {
    value: string;
    onChange: (emoji: string) => void;
    className?: string;
}

export function EmojiPicker({ value, onChange, className = '' }: EmojiPickerProps) {
    const [isOpen, setIsOpen] = useState(false);
    const [searchQuery, setSearchQuery] = useState('');
    const [activeCategory, setActiveCategory] = useState<string | null>(null);
    const pickerRef = useRef<HTMLDivElement>(null);
    const searchInputRef = useRef<HTMLInputElement>(null);

    // Close picker when clicking outside
    useEffect(() => {
        function handleClickOutside(event: MouseEvent) {
            if (pickerRef.current && !pickerRef.current.contains(event.target as Node)) {
                setIsOpen(false);
                setSearchQuery('');
                setActiveCategory(null);
            }
        }

        if (isOpen) {
            document.addEventListener('mousedown', handleClickOutside);
        }
        return () => document.removeEventListener('mousedown', handleClickOutside);
    }, [isOpen]);

    // Focus search input when picker opens
    useEffect(() => {
        if (isOpen && searchInputRef.current) {
            searchInputRef.current.focus();
        }
    }, [isOpen]);

    // Filter emojis based on search query
    const filteredEmojis = useMemo(() => {
        if (!searchQuery) return null;
        const query = searchQuery.toLowerCase();
        return ALL_EMOJIS.filter(emoji => emoji.includes(query));
    }, [searchQuery]);

    const handleSelect = (emoji: string) => {
        onChange(emoji);
        setIsOpen(false);
        setSearchQuery('');
        setActiveCategory(null);
    };

    const categories = Object.keys(EMOJI_CATEGORIES);

    return (
        <div className={`relative ${className}`} ref={pickerRef}>
            {/* Emoji Button - Shows current selection */}
            <button
                type="button"
                onClick={() => setIsOpen(!isOpen)}
                className={`text-4xl p-3 rounded-xl transition-all hover:scale-110 ${isOpen
                        ? 'bg-[var(--color-highlight)] ring-2 ring-[var(--color-highlight)]/50'
                        : 'bg-[var(--color-surface)] hover:bg-[var(--color-surface-hover)]'
                    }`}
            >
                {value}
            </button>

            {/* Picker Dropdown */}
            {isOpen && (
                <div className="absolute z-50 left-0 top-full mt-2 w-80 max-w-[calc(100vw-2rem)] bg-[var(--color-surface)] border border-[var(--color-border)] rounded-xl shadow-2xl overflow-hidden animate-scale-in">
                    {/* Search Header */}
                    <div className="p-3 border-b border-[var(--color-border)]">
                        <div className="relative">
                            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 w-4 h-4 text-[var(--color-text-muted)]" />
                            <input
                                ref={searchInputRef}
                                type="text"
                                placeholder="Search emojis..."
                                value={searchQuery}
                                onChange={(e) => setSearchQuery(e.target.value)}
                                className="w-full pl-9 pr-8 py-2 bg-[var(--color-bg-secondary)] border border-[var(--color-border)] rounded-lg text-sm focus:outline-none focus:border-[var(--color-highlight)]"
                            />
                            {searchQuery && (
                                <button
                                    onClick={() => setSearchQuery('')}
                                    className="absolute right-2 top-1/2 transform -translate-y-1/2 p-1 hover:bg-[var(--color-surface-hover)] rounded"
                                >
                                    <X className="w-3 h-3 text-[var(--color-text-muted)]" />
                                </button>
                            )}
                        </div>
                    </div>

                    {/* Emoji Content */}
                    <div className="max-h-72 overflow-y-auto">
                        {/* Search Results */}
                        {searchQuery && filteredEmojis && (
                            <div className="p-3">
                                {filteredEmojis.length > 0 ? (
                                    <>
                                        <p className="text-xs text-[var(--color-text-muted)] mb-2">
                                            {filteredEmojis.length} result{filteredEmojis.length !== 1 ? 's' : ''}
                                        </p>
                                        <div className="flex flex-wrap gap-1">
                                            {filteredEmojis.slice(0, 100).map((emoji, index) => (
                                                <button
                                                    key={`${emoji}-${index}`}
                                                    type="button"
                                                    onClick={() => handleSelect(emoji)}
                                                    className={`text-2xl p-1.5 rounded-lg transition-all hover:bg-[var(--color-highlight)] hover:scale-110 ${value === emoji ? 'bg-[var(--color-highlight)]' : ''
                                                        }`}
                                                >
                                                    {emoji}
                                                </button>
                                            ))}
                                        </div>
                                    </>
                                ) : (
                                    <p className="text-sm text-[var(--color-text-muted)] text-center py-4">
                                        No emojis found
                                    </p>
                                )}
                            </div>
                        )}

                        {/* Category View */}
                        {!searchQuery && activeCategory && (
                            <div className="p-3">
                                <button
                                    onClick={() => setActiveCategory(null)}
                                    className="flex items-center gap-1 text-sm text-[var(--color-highlight)] mb-3 hover:underline"
                                >
                                    ‚Üê Back to categories
                                </button>
                                <p className="text-xs font-medium text-[var(--color-text-muted)] mb-2">
                                    {activeCategory}
                                </p>
                                <div className="flex flex-wrap gap-1">
                                    {EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES].map((emoji, index) => (
                                        <button
                                            key={`${emoji}-${index}`}
                                            type="button"
                                            onClick={() => handleSelect(emoji)}
                                            className={`text-2xl p-1.5 rounded-lg transition-all hover:bg-[var(--color-highlight)] hover:scale-110 ${value === emoji ? 'bg-[var(--color-highlight)]' : ''
                                                }`}
                                        >
                                            {emoji}
                                        </button>
                                    ))}
                                </div>
                            </div>
                        )}

                        {/* Default View - Quick Access + Categories */}
                        {!searchQuery && !activeCategory && (
                            <div className="p-3 space-y-4">
                                {/* Quick Access */}
                                <div>
                                    <p className="text-xs font-medium text-[var(--color-text-muted)] mb-2">
                                        Quick Access
                                    </p>
                                    <div className="flex flex-wrap gap-1">
                                        {QUICK_EMOJIS.map((emoji, index) => (
                                            <button
                                                key={`quick-${emoji}-${index}`}
                                                type="button"
                                                onClick={() => handleSelect(emoji)}
                                                className={`text-2xl p-1.5 rounded-lg transition-all hover:bg-[var(--color-highlight)] hover:scale-110 ${value === emoji ? 'bg-[var(--color-highlight)]' : ''
                                                    }`}
                                            >
                                                {emoji}
                                            </button>
                                        ))}
                                    </div>
                                </div>

                                {/* Category List */}
                                <div>
                                    <p className="text-xs font-medium text-[var(--color-text-muted)] mb-2">
                                        Browse Categories
                                    </p>
                                    <div className="space-y-1">
                                        {categories.map((category) => (
                                            <button
                                                key={category}
                                                onClick={() => setActiveCategory(category)}
                                                className="w-full flex items-center justify-between px-3 py-2 rounded-lg text-left text-sm hover:bg-[var(--color-surface-hover)] transition-colors"
                                            >
                                                <span className="flex items-center gap-2">
                                                    <span className="text-lg">
                                                        {EMOJI_CATEGORIES[category as keyof typeof EMOJI_CATEGORIES][0]}
                                                    </span>
                                                    <span>{category}</span>
                                                </span>
                                                <ChevronRight className="w-4 h-4 text-[var(--color-text-muted)]" />
                                            </button>
                                        ))}
                                    </div>
                                </div>
                            </div>
                        )}
                    </div>
                </div>
            )}
        </div>
    );
}
